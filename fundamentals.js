// Data Type
// String
let namaDepan = "Steven";
let namaBelakang = "Mcguire";
let namaLengkap = namaDepan + " " + namaBelakang;
console.log("String:", namaLengkap);

// Boolean
let isOnline = true;
let isMember = false;
console.log("Boolean:", isOnline);
console.log("Boolean:", isMember);

// Number
let age = 19;
console.log("Number:", age);

// Array
let favoriteColors = ["Black"];
console.log("Array:", favoriteColors);

// Object
let Coffee = {
    nama: "Espresso",
    tahun_ditemukan: 1901,
    Oleh: "Luigi Bezzera",
};
console.log("Object:", Coffee);
